export function getTranslation(lang: LanguageCode, key: string): string {
  return translations[lang]?.[key] || translations.en[key] || key;
}

export function getLanguageName(code: LanguageCode): string {
  const lang = SUPPORTED_LANGUAGES.find((l) => l.code === code);
  return lang?.name || "English";
}

const STORAGE_KEY = "prox-language";

export function getStoredLanguage(): LanguageCode {
  if (typeof window === "undefined") return "en";
  return (localStorage.getItem(STORAGE_KEY) as LanguageCode) || "en";
}

export function setStoredLanguage(lang: LanguageCode): void {
  if (typeof window === "undefined") return;
  localStorage.setItem(STORAGE_KEY, lang);
}
